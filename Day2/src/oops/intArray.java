package oops;

public class intArray {
	public static void main(String args[]) {
		//int as variable
		int[] numbers = new int[4];
		numbers[0] = 10;
		System.out.println(numbers[1]);
		numbers[2] = 20;
		
		//Integer as wrapper class
		Integer[] num = new Integer[4];
		num[0] = 10;
		System.out.println(num[1]);
		num[2] = 20;
	}
}