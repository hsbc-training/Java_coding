package print_num;

public class numbers {

	public static void main(String[] args) {
		int num[]= {10,20,30,40,50};
		for(int y:num)
		{
			System.out.print(y);
			System.out.print(",");
		}
		System.out.println("\n");
		String days[]= {"Monday", "Tuesday", "Wednesday"};
		for(String m:days)
		{
			System.out.print(m);
			System.out.print(",");
		}

	}

}
